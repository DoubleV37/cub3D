#include <stddef.h>

void	ft_strcpy(char *dst, const char *src)
{
	size_t		i;

	i = 0;
	if (!dst)
		return ;
	while (src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
}
