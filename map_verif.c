int	iscub(char	*path)
{
	size_t	i;

	i = ft_strlen(path) - 1;
	if (path[i] == 'b' && path[i - 1] == 'u'
		&& path[i - 2] == 'c' && path[i - 3] == '.' && ft_isprint(path[i - 4]))
		return (1);
	return (0);
}
